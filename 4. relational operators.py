'''
    Relation operators
    <:  Less then
    <=: Less then equal too
    >:  Greater then
    >=: Greate then equal too
    ==: Equal too
    !=: Not equal too
'''

num1 = 10
num2= 20

print(num1 < num2)
print(num1 <= num2)
print(num1 > num2)
print(num1 >= num2)
print(num1 == num2)
print(num1 != num2)