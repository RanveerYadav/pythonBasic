''' Arithmatic operators
    +:  Addition
    -:  Subtraction
    *:  Multiplication
    /:  Division with return value in float
    //: Division with return value in int
    **: Power of a number
'''
num1 = 10
num2 = 3

# addition
num3 = num1 + num2
print("Value of num3 is", num3)

# subtraction
num3 = num1 - num2
print("Value of num3 is", num3)

# multiplication
num3 = num1 * num2
print("Value of num3 is", num3)

# division, value as float
num3 = num1 / num2
print("Value of num3 is", num3)

# division, value as int
num3 = num1 // num2
print("Value of num3 is", num3)

# power of
num3 = num1**num2
print("Value of num3 is", num3)
